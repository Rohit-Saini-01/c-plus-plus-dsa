#include <iostream>
using namespace std;

int main()
{
    int num_1 = 2;
    int num_2 = 3;

    cout << num_1 <<" + " << num_2 << " = " << num_1 + num_2 << endl;
    cout << num_1 <<" - " << num_2 << " = " << num_1 - num_2 << endl;
    cout << num_1 <<" * " << num_2 << " = " << num_1 * num_2 << endl;
    cout << num_1 <<" / " << num_2 << " = " << num_1 / num_2 << endl;
    cout << num_1 <<" % " << num_2 << " = " << num_1 % num_2 << endl;

    return 0;
}